package myProject;

public class OtraClase {
    private int val;

    public OtraClase(int val) {
        this.val = val;
    }

    public int getVal() {
        return val;
    }

    public void setVal(int val) {
        this.val = val;
    }

    @Override
    public String toString() {
        return "OtraClase{" +
                "val=" + val +
                '}';
    }
}
